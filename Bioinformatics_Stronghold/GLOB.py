def GlobalAlignment(v,w,sigma,scoring_matrix):
    n,m = len(v),len(w)
    s = [[0 for j in range(m+1)] for i in range(n+1)]
    for i in range(1,n+1):
        s[i][0] = s[i-1][0] - sigma
    for j in range(1,m+1):
        s[0][j] = s[0][j-1] - sigma
    for i in range(1,n+1):
        for j in range(1,m+1):
            score = scoring_matrix[alphabet.index(v[i-1])][alphabet.index(w[j-1])]
            s[i][j] = max(s[i-1][j] - sigma, s[i][j-1] - sigma, s[i-1][j-1] + score)
    return s[-1][-1]

